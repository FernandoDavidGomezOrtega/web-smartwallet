    <h2>Cookies que usamos</h2>
    <p>
    Smart Wallet utiliza diferentes tipos de cookies en su sitio web para hacer que la experiencia del usuario sea más agradable, memorizar sus preferencias y personalizar sus ofertas.
    </p>
    <p>
    Nuestro sitio web puede colocar los siguientes tipos de cookies:
    </p>
    <ul id="" class="pl-3">
      <li>Cookies analíticas / de rendimiento : nos permiten recopilar datos estadísticos relacionados con su uso de nuestro sitio web. Dichos datos contribuyen a la mejora continua del funcionamiento de nuestro sitio web, en particular al garantizar que los usuarios encuentren fácilmente lo que están buscando.</li>
      <li>Cookies de funcionalidad : estas cookies son estrictamente necesarias para el uso adecuado de nuestro sitio web. Se utilizan para reconocerlo cuando regresa a nuestro sitio web, lo que nos permite personalizar el contenido para usted y, en general, para proporcionarle nuestros servicios (como las cookies llamadas "carrito de compras").</li>
      <li>Cookies de orientación : estas cookies registran su visita a nuestro sitio web, las páginas que ha visitado y los enlaces que ha seguido. Utilizaremos esta información para hacer que nuestro sitio web y la publicidad que se muestra en él sean más relevantes para sus intereses. También podemos compartir esta información con terceros para este propósito.</li>
    </ul>

    <h2>Su gestión de cookies</h2>
    <p>
    El registro de cookies en su dispositivo está sujeto a su consentimiento previo. Antes de colocar cookies en su computadora, nuestro sitio web le enviará un mensaje solicitando su consentimiento para configurar estas cookies (en forma de un banner, que aparecerá durante su primera visita). Al consentir en colocar dichas Cookies, usted le brinda a Smart Wallet y / o nuestros socios la oportunidad de mejorar nuestros servicios y brindarle una experiencia más agradable como Usuario de nuestro sitio web. También puede administrar el uso de cookies en cualquier momento configurando el software de su navegador, utilizando la herramienta Ghostery o desactivando las cookies.
    </p>
    <p>
    Puede elegir eliminar las cookies en cualquier momento; sin embargo, puede perder información que le permita acceder a nuestro sitio web de manera más rápida y eficiente, incluidas, entre otras, las configuraciones de personalización. Le recomendamos que se asegure de que su navegador web esté actualizado y que consulte el menú de 'ayuda' y los consejos proporcionados por el desarrollador de su navegador si no sabe cómo ajustar su configuración de privacidad.
    </p>

    <h2>¿Cuánto duran las cookies?</h2>
